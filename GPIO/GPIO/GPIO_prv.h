#ifndef GPIO_PRV_H_
#define GPIO_PRV_H_

/*****************************************************************************************************************************************
********************************************************GPIO base address -- AFIO base address**************************************************
*****************************************************************************************************************************************/
#define GPIOA_BASE_ADDRESS             	 ((volatile u32*)(0x40010800))
#define GPIOB_BASE_ADDRESS             	 ((volatile u32*)(0x40010C00))
#define GPIOC_BASE_ADDRESS               ((volatile u32*)(0x40011000))

#define AFIO_BASE_ADDRESS              	 ((volatile u32*)(40010000))

/*****************************************************************************************************************************************
*********************************************************GPIO, AFIO Peripherals macros********************************************************
*****************************************************************************************************************************************/
#define GPIOA             				 ((GPIO_registersMap*)(GPIOA_BASE_ADDRESS))
#define GPIOB             				 ((GPIO_registersMap*)(GPIOB_BASE_ADDRESS))
#define GPIOC            				 ((GPIO_registersMap*)(GPIOC_BASE_ADDRESS))

#define AFIO            				 ((GPIO_AFregistersMap*)(AFIO_BASE_ADDRESS))

/*****************************************************************************************************************************************
****************************************************GPIO registers -- AFIO registers ***********************************************************
*****************************************************************************************************************************************/
typedef struct
{
	volatile u32 GPIO_CRL;
	volatile u32 GPIO_CRH;
	volatile u32 GPIO_IDR;
	volatile u32 GPIO_ODR;
	volatile u32 GPIO_BSRR;
	volatile u32 GPIO_BRR;
}GPIO_registersMap;

typedef struct
{
	volatile u32 AFIO_EVCR;
	volatile u32 AFIO_MAPR;
	volatile u32 AFIO_EXTICR1;
	volatile u32 AFIO_EXTICR2;
	volatile u32 AFIO_EXTICR3;
	volatile u32 AFIO_EXTICR4;
	volatile u32 reserved;
	volatile u32 AFIO_MAPR2;
}GPIO_AFregistersMap;

/*****************************************************************************************************************************************
***************************************************************static cofigurations**************************************************************
*****************************************************************************************************************************************/
#define GPIO_NUM_OF_PORTS                            (2U)
#define GPIO_NUM_OF_PINS_PER_PORT                    (16U)


#endif /* GPIO_PRV_H_ */
